def afficher_type_developpeur(langage):
    if langage == "javascript":
        print("Tu es un développeur web")
    elif langage == "python":
        print("Tu es un développeur IA")
    elif langage == "java":
        print("Tu es un développeur logiciel")
    elif langage == "reactjs":
        print("Tu es un développeur mobile")
    else:
        print("Un jour, je serai le meilleur développeur...")

afficher_type_developpeur("javascript")
afficher_type_developpeur("python")
afficher_type_developpeur("java")
afficher_type_developpeur("reactjs")
afficher_type_developpeur("c++")